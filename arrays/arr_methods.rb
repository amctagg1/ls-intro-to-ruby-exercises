# What does each method return in the following example?

arr = [15, 7, 18, 5, 12, 8, 5, 1]

puts "arr.index(5) will get the index position of the first occurrence of the value 5 >> 3"
puts arr.index(5)

puts "arr.index[5] is not valid syntax and will throw an error"

puts "arr[5] will get the value from index position 5 >> 8"
puts arr[5]
